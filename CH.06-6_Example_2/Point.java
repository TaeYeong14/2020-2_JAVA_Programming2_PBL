public class Point
{
    // 인스턴스 변수 - 다음의 예제를 사용자에 맞게 바꾸십시오
    private int x, y;

    public Point(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    
    public String toString()
    {
        return "Point(" + x + "," + y + ")";
    }
}
